package mini_project;

public class soccerstars{
	private String name;
	private int age;
	private String height;
	
	public soccerstars(String name, int age, String height) {
		this.name = name;
		this.age = age;
		this.height = height;
	}
	
	public String getName() {
		return name;
	}
	
	public int getAge() {
		return age;
	}
	
	public String getheight() {
		return height;
	}
}